
public class Arithmetic {
	static void add()
	{
		int a = 10;
		int b= 5;
		int c = a+b;
		System.out.println("The addition result is"+c);
	}
		static void sub()
	{
		int a =18;
		int b= 8;
		int c = a-b;
		System.out.println("Value after substraction is " +c);
	}
	static void mul()
	{
	int a =4;
	int b= 2;
	int c = a*b;
	System.out.println("Value after Multiplication is " +c);
	}
	static void div()
	{
	int a =18;
	int b= 3;
	int c = a/b;
	System.out.println("Value after Division is " +c);
	}
	public static void main (String[] args)
	{
		add(); sub(); mul(); div();
				
	}
	
}
